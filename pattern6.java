public class pattern6 {
    public static void main(String args[])
    {
        for(int i =0;i<6;i++)
        {
            for(int j=5;j>=i;j--)
            {
                System.out.print(" ");
            }
            for(int k =i;k>=1;k--)
            {
                System.out.print("*");
            }
            for(int a =2;a<=i;a++)
            {
                System.out.print("*");    
            }
            System.out.println();
        }
        for(int t=5;t>=1;t--)
        {
             for(int b=4;b<=5;b--)
            {
                System.out.print(" ");
            }
            for(int c=4;c>=1;c--)
            {
                System.out.print("*");
            }
            for(int d=3;d>=t;d--)
            {
                System.out.print("*");    
            }
            System.out.println();
            }
        }
    }
    